package CSARCH2;

public class Steps {
    private String Step;
    private String A;
    private String Q;

    public Steps(String Step, String A, String Q) {
        this.Step = Step;
        this.A = A;
        this.Q = Q;
    }

    public String getStep() {
        return Step;
    }

    public String getA() {
        return A;
    }

    public String getQ() {
        return Q;
    }

}
